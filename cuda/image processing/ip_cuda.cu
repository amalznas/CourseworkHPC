  int m, n, o, p; // the pixels adjacent to x,y used for the calculation
  int w; // the result of calculate
    
    y = i / width;
    x = i - (width * y);

    if (x == 0 || y == 0 || x == width - 1 || y == height - 1) {
      out[i] = 0;
    } else {
      m = i + width;
      n = i - 1;
      o = i + 1;
      p = i - width;

      w = (in[i] * 4) + (in[m] * -1) + (in[n] * -1) + (in[o] * -1)
          + (in[p] * -1);

      if (w > 0) { // if the result is positive this is an edge pixel
        out[i] = 255;
      } else {
        out[i] = 0;
      }
    }
  }


